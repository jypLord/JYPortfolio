export function normalizeKrStockName(name) {
  return String(name ?? "")
    .trim()
    .replace(/\s+/g, "")
    .toUpperCase();
}

export const NORMALIZED_KR_STOCK_NAME_SET = new Set(
  [...KR_STOCK_NAME_SET].map(normalizeKrStockName)
);

export function isValidStockName(name) {
  return NORMALIZED_KR_STOCK_NAME_SET.has(normalizeKrStockName(name));
}
